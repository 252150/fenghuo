package com.wanjia.infoshopmallmanage.entity;

import java.util.Date;

/**
 * (ChartImage)实体类
 *
 * @author: wlj
 * @date: 2020-09-27 13:33:44
 */
public class ChartImage {
    
    private Long id;
    /**
    * 图片
    */
    private String chartImage;
    /**
    * 类型：1:启动页；0:轮播图 
    */
    private Integer chartType;
    
    private String chartAddr;
    
    private Date createTime;

        
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
        
    public String getChartImage() {
        return chartImage;
    }

    public void setChartImage(String chartImage) {
        this.chartImage = chartImage;
    }
        
    public Integer getChartType() {
        return chartType;
    }

    public void setChartType(Integer chartType) {
        this.chartType = chartType;
    }
        
    public String getChartAddr() {
        return chartAddr;
    }

    public void setChartAddr(String chartAddr) {
        this.chartAddr = chartAddr;
    }
        
    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

}