package com.wanjia.infoshopmallmanage.entity;

import java.math.BigDecimal;

/**
 * (OrderDetail)实体类
 *
 * @author: wlj
 * @date: 2020-09-27 13:33:45
 */
public class OrderDetail {
    
    private Long id;
    /**
    * 订单id
    */
    private Long orderId;
    /**
    * 商品名称
    */
    private String productName;
    /**
    * 推荐图 
    */
    private String recommendImage;
    /**
    * 商品数量
    */
    private Integer num;
    /**
    * 商品价格
    */
    private BigDecimal productPrice;
    /**
    * 实际支付价格
    */
    private BigDecimal payPrice;
    /**
    * 商品id
    */
    private Long goodsId;
    /**
    * 规格
    */
    private String specification;
    /**
    * 使用积分数量
    */
    private Integer usePoints;
    /**
    * 是否评论 0：未评论；1：已评论
    */
    private Integer isComment;

        
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
        
    public Long getOrderId() {
        return orderId;
    }

    public void setOrderId(Long orderId) {
        this.orderId = orderId;
    }
        
    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }
        
    public String getRecommendImage() {
        return recommendImage;
    }

    public void setRecommendImage(String recommendImage) {
        this.recommendImage = recommendImage;
    }
        
    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }
        
    public BigDecimal getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(BigDecimal productPrice) {
        this.productPrice = productPrice;
    }
        
    public BigDecimal getPayPrice() {
        return payPrice;
    }

    public void setPayPrice(BigDecimal payPrice) {
        this.payPrice = payPrice;
    }
        
    public Long getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(Long goodsId) {
        this.goodsId = goodsId;
    }
        
    public String getSpecification() {
        return specification;
    }

    public void setSpecification(String specification) {
        this.specification = specification;
    }
        
    public Integer getUsePoints() {
        return usePoints;
    }

    public void setUsePoints(Integer usePoints) {
        this.usePoints = usePoints;
    }
        
    public Integer getIsComment() {
        return isComment;
    }

    public void setIsComment(Integer isComment) {
        this.isComment = isComment;
    }

}