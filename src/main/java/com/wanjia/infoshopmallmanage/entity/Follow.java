package com.wanjia.infoshopmallmanage.entity;

import java.util.Date;

/**
 * (Follow)实体类
 *
 * @author: wlj
 * @date: 2020-09-27 13:33:44
 */
public class Follow {
    
    private Long id;
    /**
    * 关注人id
    */
    private Long followerId;
    /**
    * 被关注人id
    */
    private Long concernerId;
    /**
    * 关注时间
    */
    private Date followTime;
    /**
    * 是否双向关注: 0:否；1：是
    */
    private Integer eachotherAttention;
    /**
    * 好友分组id
    */
    private Long friendGroupId;

        
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
        
    public Long getFollowerId() {
        return followerId;
    }

    public void setFollowerId(Long followerId) {
        this.followerId = followerId;
    }
        
    public Long getConcernerId() {
        return concernerId;
    }

    public void setConcernerId(Long concernerId) {
        this.concernerId = concernerId;
    }
        
    public Date getFollowTime() {
        return followTime;
    }

    public void setFollowTime(Date followTime) {
        this.followTime = followTime;
    }
        
    public Integer getEachotherAttention() {
        return eachotherAttention;
    }

    public void setEachotherAttention(Integer eachotherAttention) {
        this.eachotherAttention = eachotherAttention;
    }
        
    public Long getFriendGroupId() {
        return friendGroupId;
    }

    public void setFriendGroupId(Long friendGroupId) {
        this.friendGroupId = friendGroupId;
    }

}