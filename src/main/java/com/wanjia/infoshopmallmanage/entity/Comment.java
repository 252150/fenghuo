package com.wanjia.infoshopmallmanage.entity;

import java.util.Date;

/**
 * (Comment)实体类
 *
 * @author: wlj
 * @date: 2020-09-27 13:33:44
 */
public class Comment {
    
    private Long id;
    /**
    * 用户id
    */
    private Long userId;
    /**
    * 订单id
    */
    private Long orderId;
    /**
    * 商品id
    */
    private Long goodsId;
    /**
    * 唯一id(用户订单id和商品id组合而成)
    */
    private String onlyId;
    /**
    * 商品类型: 0:普通商品 1:秒杀商品
    */
    private Integer goodsType;
    /**
    * 分数
    */
    private Integer goodsScore;
    /**
    * 服务分数
    */
    private Integer serviceScore;
    /**
    * 评论内容
    */
    private String commentContent;
    /**
    * 评论图片
    */
    private String commentImages;
    /**
    * 评论时间
    */
    private Date commentTime;
    /**
    * 是否追评： 0：否；1：是
    */
    private Integer isAdditional;

        
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
        
    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }
        
    public Long getOrderId() {
        return orderId;
    }

    public void setOrderId(Long orderId) {
        this.orderId = orderId;
    }
        
    public Long getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(Long goodsId) {
        this.goodsId = goodsId;
    }
        
    public String getOnlyId() {
        return onlyId;
    }

    public void setOnlyId(String onlyId) {
        this.onlyId = onlyId;
    }
        
    public Integer getGoodsType() {
        return goodsType;
    }

    public void setGoodsType(Integer goodsType) {
        this.goodsType = goodsType;
    }
        
    public Integer getGoodsScore() {
        return goodsScore;
    }

    public void setGoodsScore(Integer goodsScore) {
        this.goodsScore = goodsScore;
    }
        
    public Integer getServiceScore() {
        return serviceScore;
    }

    public void setServiceScore(Integer serviceScore) {
        this.serviceScore = serviceScore;
    }
        
    public String getCommentContent() {
        return commentContent;
    }

    public void setCommentContent(String commentContent) {
        this.commentContent = commentContent;
    }
        
    public String getCommentImages() {
        return commentImages;
    }

    public void setCommentImages(String commentImages) {
        this.commentImages = commentImages;
    }
        
    public Date getCommentTime() {
        return commentTime;
    }

    public void setCommentTime(Date commentTime) {
        this.commentTime = commentTime;
    }
        
    public Integer getIsAdditional() {
        return isAdditional;
    }

    public void setIsAdditional(Integer isAdditional) {
        this.isAdditional = isAdditional;
    }

}